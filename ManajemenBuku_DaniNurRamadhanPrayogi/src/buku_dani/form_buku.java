    private void load_table(){
        DefaultTableModel obyekTable = new DefaultTableModel();
        obyekTable.addColumn("Id Buku");
        obyekTable.addColumn("Judul");
        obyekTable.addColumn("Penerbit");
        obyekTable.addColumn("Pengarang");
        obyekTable.addColumn("Gambar");

        
        try{
            String sql = "select * from buku";
            java.sql.Connection conn=(Connection) koneksi.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                obyekTable.addRow(new Object [] {res.getString(1),
                res.getString(2),
                res.getString(3),
                res.getString(4),
                res.getString(5)});
            }
            t_buku.setModel (obyekTable);
        }catch(Exception e){
            
        }
    
    
    }
    
    
    public void cari_data(String key){
        try{
            Object[] Kolom = {"ID Buku", "Judul", "Penerbit", "Pengarang","Gambar"};
            DefaultTableModel obyektable = new DefaultTableModel();
            obyektable = new DefaultTableModel(null, Kolom);
            obyektable.getDataVector().removeAllElements();

            Connection c = koneksi.configDB();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM buku WHERE id_buku LIKE '%"+key+"%' "
                    + "OR judul LIKE '%"+key+"%' "
                    + "OR penerbit LIKE '%"+key+"%' "
                    + "OR pengarang LIKE '%"+key+"%'";
            ResultSet rs = s.executeQuery(sql);
            
            while(rs.next()){
                obyektable.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5)
                });
                t_buku.setModel(obyektable);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    
