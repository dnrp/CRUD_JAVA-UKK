    private void t_bukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_bukuMouseClicked
        // TODO add your handling code here:
        int baris = t_buku.rowAtPoint(evt.getPoint());
        String id = t_buku.getValueAt(baris, 0).toString();
        txt_id_buku.setText(id);
        String judul = t_buku.getValueAt(baris, 1).toString();
        txt_judul.setText(judul);
        String penerbit = t_buku.getValueAt(baris, 2).toString();
        txt_penerbit.setText(penerbit);
        String pengarang = t_buku.getValueAt(baris, 3).toString();
        txt_pengarang.setText(pengarang);
        String gambar = t_buku.getValueAt(baris, 4).toString();
        txt_gambar.setText(gambar);
        txt_id_buku.setEditable(false);
    }//GEN-LAST:event_t_bukuMouseClicked

